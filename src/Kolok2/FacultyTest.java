import java.util.*;

class OperationNotAllowedException extends Exception {
    public OperationNotAllowedException(String message) {
        super(message);
    }
}

class Course {

    String name;
    int studentsCount;
    int totalGrades;

    public Course(String name) {
        this.name = name;
        this.studentsCount = 0;
        this.totalGrades = 0;
    }

    public String getName() {
        return name;
    }

    public void addGrade(int grade) {
        totalGrades+=grade;
        studentsCount++;
    }

    public double getAverageGrade() {
        if(studentsCount > 0) {
            return (double) totalGrades / studentsCount;
        }
        return 0;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Course course = (Course) o;
        return studentsCount == course.studentsCount && totalGrades == course.totalGrades && Objects.equals(name, course.name);
    }

    @Override
    public int hashCode() {
        int result = Objects.hashCode(name);
        result = 31 * result + studentsCount;
        result = 31 * result + totalGrades;
        return result;
    }

    @Override
    public String toString() {
        return String.format("%s %d %.2f", name,totalGrades,getAverageGrade());
    }
}

class Student {

    String id;
    int yearsOfStudies;
    Map<Integer,Map<String,Integer>> grades;
    boolean graduated;

    public Student(String id, int yearsOfStudies) {
        this.id = id;
        this.yearsOfStudies = yearsOfStudies;
        grades = new HashMap<>();
        graduated = false;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getYearsOfStudies() {
        return yearsOfStudies;
    }

    public void setYearsOfStudies(int yearsOfStudies) {
        this.yearsOfStudies = yearsOfStudies;
    }

    public void addGrade(int term, String courseName, int grade) throws OperationNotAllowedException {
        if(graduated) {
            throw new OperationNotAllowedException(String.format("Student %s is already graduated", id));
        }
        if(grades.containsKey(term) && grades.get(term).size() >= 3) {
            throw new OperationNotAllowedException(String.format("Student %s already has 3 grades in term %s", id,term));
        }
        if(term > yearsOfStudies * 2) {
            throw new OperationNotAllowedException(String.format("Term %s is not possible for student with ID %s",term,id));
        }
        grades.put(term,new HashMap<>());
        grades.get(term).put(courseName,grade);
    }

    public boolean isGraduated() {

    }

    @Override
    public String toString() {
        return null;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Student student = (Student) o;
        return yearsOfStudies == student.yearsOfStudies && Objects.equals(id, student.id);
    }

    @Override
    public int hashCode() {
        int result = Objects.hashCode(id);
        result = 31 * result + yearsOfStudies;
        return result;
    }
}

